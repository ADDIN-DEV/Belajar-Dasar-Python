## Operasi Aritmatika

a = 10

b = 3

c= 13

d = 5

#tambah = +
print(a, '+', b, '=', a+b)

#kurang = -
print(a, '-', b, '=', a-b)

#kali = *
print(a, "x", b, '=', a*b)

#bagi = /
print(a, '/', b, '=', a/b)

#modulus = %
print(a, '%', b, '=', a%b)

#pangkat / Eksponen = **
print(a, '^', b, '=', a**b)

#pembagian bulat or Floor division = //
print(a, '//', b, '=', a//b)
# Untuk mengetahui hasil pembagian bulat, maka gunakan //, bukan /. agar hasilnya tidak menjadi float

#Perioritas Operasi Aritmatika
Hasil = c + a * d / b
print(c ,'+', a ,'*', d ,'/', b,'=', Hasil)
#jika ada kurung (), maka operasi di dalam kurung akan dijalankan terlebih dahulu

